package certLeader;

public class _35Switch {
    public static void main(String[] args) {
        String opt="true";
        switch (opt){
            case "true":
                System.out.println("True");
                break;
            case "false":
                System.out.println("False");
                break;
            default:
                System.out.println("****");
        }
        System.out.println("Done");

    }
}
