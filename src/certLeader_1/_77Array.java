package certLeader_1;

public class _77Array {
    public static void main(String[] args) {
        int[]intArr={8,16,32,64,128};
        for(int each:intArr){
            System.out.print(each+" ");
        }
        System.out.println();
        for (int i = 0; i < intArr.length; i++) {
            System.out.print(intArr[i]+" ");
        }
    }
}
